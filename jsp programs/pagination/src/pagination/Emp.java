package pagination;

public class Emp {
	private int id;  
	private String name;  
	private float salary;
	
	
	public int getid() {
		return id;
	}
	public void setid(int id) {
		this.id=id;
	}
	public String getname() {
		return name;
	}
	public void setname(String name) {
		this.name=name;
	}
	public float getsalary() {
		return salary;
	}
	public void setsalary(float salary) {
		this.salary=salary;
	}

}
